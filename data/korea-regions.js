export const getProvinces = () => {
  return Object.keys(KOREA_REGIONS);
};

export const getDistricts = (province) => {
  if (!KOREA_REGIONS[province]) return [];
  return Object.keys(KOREA_REGIONS[province].districts);
};

export const getDongs = (province, district) => {
  if (!KOREA_REGIONS[province] || !KOREA_REGIONS[province].districts[district]) return [];
  return KOREA_REGIONS[province].districts[district].dongs || [];
};

export const getRegionCode = (province, district) => {
  if (!KOREA_REGIONS[province] || !KOREA_REGIONS[province].districts[district]) return null;
  return KOREA_REGIONS[province].districts[district].code;
};

